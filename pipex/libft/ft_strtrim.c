#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	int		i;
	char	*nova;

	if (!s1)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1) - 1;
	while (end > start && ft_strchr(set, s1[end]))
		end--;
	nova = (char *)malloc(end - start + 2);
	if (!nova)
		return (NULL);
	i = 0;
	while (start <= end)
	{
		nova[i] = s1[start];
		i++;
		start++;
	}
	nova[i] = '\0';
	return (nova);
}
